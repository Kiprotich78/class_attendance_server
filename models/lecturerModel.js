const mongoose = require('mongoose');

const lecturerSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const lecturer = mongoose.model('lecturer', lecturerSchema);

module.exports = lecturer;
